void main()
{
	bool b;

	b = true;
	b = false;

	b = 1; // 1 is equivalent to true
	b = 0; // 0 is equivalent to false
	b = -123;
}

/*
In C++ except 0, all positive and negative
numbers are treated as true.

0 is treated as false.

One can also use boolean constants 'true' and
'false'.

It is recommended to use boolean constants
instead of integers as boolean values.

*/